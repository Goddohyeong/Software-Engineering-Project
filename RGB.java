
public class RGB {
	int red;
	int green;
	int blue;
	
	public RGB()
	{
		red=0;
		green=0;
		blue=0;
	}
	
	public int get_red()
	{
		return red;
	}
	public int get_green()
	{
		return green;
	}
	public int get_blue()
	{
		return blue;
	}
	public void set_red(int red)
	{
		this.red=red;
	}
	public void set_green(int green)
	{
		this.green=green;
	}
	public void set_blue(int blue)
	{
		this.blue=blue;
	}
}
